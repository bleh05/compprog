	static class Task {
		static final int inf = Integer.MAX_VALUE;

		public void solve(int testNumber, FastReader sc, PrintWriter pw) {
			//CHECK FOR QUICKSORT TLE
			//***********************//
			//CHECK FOR INT OVERFLOW
			//***********************//
			int n = sc.nextInt();
			String m = sc.next();
			long[] dp = new long[m.length()];
			Arrays.fill(dp, Long.MAX_VALUE);
			for(int i = 0; i < m.length(); i++) {
				for(int j = i; j >= Math.max(0, i-18); j--) {
					if(j != i && m.charAt(j) == '0') continue;
					long ch = Long.parseLong(m.substring(j, i + 1));
					if(ch >= n) {
						break;
					}
					else {
						if(j > 0) {
							if(Math.sqrt(dp[j-1]) * Math.sqrt(n) <= 1e9)
								dp[i] = Math.min(dp[j-1] * n + ch, dp[i]);
						}
						else {
							dp[i] = Math.min(dp[i], ch);
						}
					}
					//pw.println(ch + " " + i);
				}
			}
			pw.println(dp[m.length()-1]);
		}
	}
	static long binpow(long a, long b, long m) {
		a %= m;
		long res = 1;
		while (b > 0) {
			if ((b & 1) == 1)
				res = res * a % m;
			a = a * a % m;
			b >>= 1;
		}
		return res;
	}

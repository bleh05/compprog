		public void solve(int testNumber, FastReader sc, PrintWriter pw) {
			//CHECK FOR QUICKSORT TLE
			//***********************//
			//CHECK FOR INT OVERFLOW
			//***********************//
			int n = sc.nextInt();
			int[] arr = new int[n];
			for(int i = 0; i < n; i++){
				arr[i] = sc.nextInt();
			}
			long a = 0;
			long b = 0;
			long c = 0;
			long d = 0;
			int turn = 1;
			int cc = 0;
			int ptr1 = 0;
			int ptr2 = n - 1;
			for(int i = 0; i < n; i++) {
				if(turn == 1){
					c += arr[ptr1++];
					if(ptr2 == ptr1 - 1){
						a += c;
						cc++;
						break;
					}
				}
				if(turn == 1 && c > d){
					turn = 2;
					d = 0;
					a += c;
					cc++;
				}
				if(turn == 2){
					d += arr[ptr2--];
					if(ptr2 == ptr1 - 1){
						b += d;
						cc++;
						break;
					}
				}
				if(turn == 2 && d > c){
					turn = 1;
					c = 0;
					b += d;
					cc++;
				}
			}
			pw.printf("%d %d %d%n", cc, a, b);
		}

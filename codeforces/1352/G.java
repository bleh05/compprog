	static class Task {
		static final int inf = Integer.MAX_VALUE;

		public void solve(int testNumber, FastReader sc, PrintWriter pw) {
			//CHECK FOR QUICKSORT TLE
			//***********************//
			//CHECK FOR INT OVERFLOW
			//***********************//
			int n = sc.nextInt();
			if(n <= 3){
				pw.println(-1);
				return;
			}
			int[] arr = new int[n];
			for(int i = 0; i < n - 3; i += 4){
				arr[i] = i + 2;
				arr[i + 1] = i + 4;
				arr[i + 2] = i + 1;
				arr[i + 3] = i + 3;
			}
			if(n % 4 == 3){
				arr[n - 4] = n - 2;
				arr[n - 3] = n;
				arr[n - 2] = n - 4;
				arr[n - 1] = n - 1;
			}
			if(n % 4 == 2){
				arr[n - 3] = n - 1;
				arr[n - 2] = n - 3;
				arr[n - 1] = n;
			}
			if(n % 4 == 1){
				arr[n - 1] = n;
			}
			for(int x : arr){
				pw.print(x + " ");
			}
			pw.println();
		}
	}
	static long binpow(long a, long b, long m) {
		a %= m;
		long res = 1;
		while (b > 0) {
			if ((b & 1) == 1)
				res = res * a % m;
			a = a * a % m;
			b >>= 1;
		}
		return res;
	}
